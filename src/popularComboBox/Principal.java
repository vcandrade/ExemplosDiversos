    public void buscarCidades() {
        
        try {
            
            File arq = new File("");
            
            BufferedReader documento = new BufferedReader(new FileReader(arq.getAbsolutePath() + "\\src\\popularComboBox\\Cidades.txt"));

            String nomeCidade = documento.readLine();
            
            while(nomeCidade != null) {
                
                this.cbCidades.addItem(nomeCidade);
                nomeCidade = documento.readLine();
            }

            documento.close();
            

        } catch (FileNotFoundException ex) {
            
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        
        } catch (IOException ex) {
            
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

